using System;

class ForLoopTask
{
    static void Main()
    {
        // Example 1: for loop
        // الهدف: طباعة الأرقام من 1 إلى 5 باستخدام for loop
        
        
        Console.WriteLine("Numbers from 1 to 10:");
        for (int i = 1; i <= 10; i++)
        {
            Console.WriteLine(i);
        }
    }
}
